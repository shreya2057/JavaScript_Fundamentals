// Storing data or value temporarily, value can be chaged
// let -> keyword that is used to create a variable
//variable can have character, number (except in 1st position), $ and _

// snake casing -> user_name
// camel casing -> userName

// integer or number
let number = 5
console.log(number)

// String
let user = "Shreya"
console.log(user)
let data = "Shreya's text"
let info = '"Shreya said"'
console.log(data)
console.log(info)

number = 7
